fn write_header(out: &mut String, config: &GoCodegenConfig) {
    out.push_str("// Code generated by Infergen. Do not edit — run `infergen generate` to regenerate.\n");
    out.push_str(&format!(
        "// infergen-core v{} · catalog schema v{}\n",
        env!("CARGO_PKG_VERSION"),
        CATALOG_SCHEMA_VERSION,
    ));
    out.push('\n');
    out.push_str(&format!("package {}\n", config.package_name));
}

fn write_imports(out: &mut String) {
    out.push('\n');
    out.push_str("import (\n");
    out.push_str("\t\"encoding/json\"\n");
    out.push_str("\t\"sync\"\n");
    out.push_str(")\n");
}

fn write_provider_interface(out: &mut String) {
    out.push('\n');
    out.push_str("// Provider receives analytics events.\n");
    out.push_str("type Provider interface {\n");
    out.push_str("\t// ID returns a unique identifier for this provider, e.g. \"posthog\".\n");
    out.push_str("\tID() string\n");
    out.push_str("\t// Track sends a single analytics event.\n");
    out.push_str("\tTrack(eventName string, properties map[string]any)\n");
    out.push_str("\t// Flush flushes any buffered events (optional — may be a no-op).\n");
    out.push_str("\tFlush()\n");
    out.push_str("\t// Shutdown performs a graceful shutdown (optional — may be a no-op).\n");
    out.push_str("\tShutdown()\n");
    out.push_str("}\n");
    out.push('\n');
    out.push_str("// Config holds the runtime configuration passed to ConfigureInfergen.\n");
    out.push_str("type Config struct {\n");
    out.push_str("\t// Providers are the analytics destinations that receive every Track call.\n");
    out.push_str("\tProviders []Provider\n");
    out.push_str("\t// Consent sets the initial consent state. Default: ConsentUnknown (permissive).\n");
    out.push_str("\tConsent ConsentState\n");
    out.push_str("\t// Region sets the initial region tag for provider routing (e.g. \"eu\", \"us\").\n");
    out.push_str("\tRegion string\n");
    out.push_str("\t// RedactFn is an optional per-property redaction hook.\n");
    out.push_str("\tRedactFn RedactFn\n");
    out.push_str("}\n");
}

fn write_runtime_state(out: &mut String) {
    out.push('\n');
    out.push_str("// _mu guards all runtime state.\n");
    out.push_str("var _mu sync.RWMutex\n");
    out.push('\n');
    out.push_str("var (\n");
    out.push_str("\t_providers     []Provider\n");
    out.push_str("\t_consentState  ConsentState\n");
    out.push_str("\t_region        string\n");
    out.push_str("\t_redactFn      RedactFn\n");
    out.push_str(")\n");
}

fn write_consent_module(out: &mut String) {
    out.push('\n');
    out.push_str("// ConsentState describes the user's analytics consent.\n");
    out.push_str("type ConsentState string\n");
    out.push('\n');
    out.push_str("const (\n");
    out.push_str("\t// ConsentUnknown is the default: events are sent until consent is explicitly denied.\n");
    out.push_str("\tConsentUnknown ConsentState = \"unknown\"\n");
    out.push_str("\t// ConsentGranted means the user has opted in.\n");
    out.push_str("\tConsentGranted ConsentState = \"granted\"\n");
    out.push_str("\t// ConsentDenied means the user has opted out; events are dropped.\n");
    out.push_str("\tConsentDenied ConsentState = \"denied\"\n");
    out.push_str(")\n");
    out.push('\n');
    out.push_str("// RedactFn is a hook called for every property before dispatch.\n");
    out.push_str("// Return the (possibly redacted) value for eventName.propName.\n");
    out.push_str("type RedactFn func(eventName, propName string, value any) any\n");
    out.push('\n');
    out.push_str("// SetConsent updates the consent state.\n");
    out.push_str("func SetConsent(state ConsentState) {\n");
    out.push_str("\t_mu.Lock()\n");
    out.push_str("\t_consentState = state\n");
    out.push_str("\t_mu.Unlock()\n");
    out.push_str("}\n");
    out.push('\n');
    out.push_str("// OptIn grants analytics consent.\n");
    out.push_str("func OptIn() { SetConsent(ConsentGranted) }\n");
    out.push('\n');
    out.push_str("// OptOut denies analytics consent; subsequent events are dropped.\n");
    out.push_str("func OptOut() { SetConsent(ConsentDenied) }\n");
    out.push('\n');
    out.push_str("// SetRegion sets a region tag used for provider routing.\n");
    out.push_str("func SetRegion(region string) {\n");
    out.push_str("\t_mu.Lock()\n");
    out.push_str("\t_region = region\n");
    out.push_str("\t_mu.Unlock()\n");
    out.push_str("}\n");
    out.push('\n');
    out.push_str("// SetRedactFn installs a per-property redaction hook.\n");
    out.push_str("func SetRedactFn(fn RedactFn) {\n");
    out.push_str("\t_mu.Lock()\n");
    out.push_str("\t_redactFn = fn\n");
    out.push_str("\t_mu.Unlock()\n");
    out.push_str("}\n");
}

fn write_configure_fn(out: &mut String) {
    out.push('\n');
    out.push_str("// ConfigureInfergen initialises the Infergen runtime.\n");
    out.push_str("// Call once before any Track calls, e.g. in main() or TestMain.\n");
    out.push_str("func ConfigureInfergen(cfg Config) {\n");
    out.push_str("\t_mu.Lock()\n");
    out.push_str("\t_providers = cfg.Providers\n");
    out.push_str("\t_mu.Unlock()\n");
    out.push_str("\tif cfg.Consent != \"\" {\n");
    out.push_str("\t\tSetConsent(cfg.Consent)\n");
    out.push_str("\t}\n");
    out.push_str("\tif cfg.Region != \"\" {\n");
    out.push_str("\t\tSetRegion(cfg.Region)\n");
    out.push_str("\t}\n");
    out.push_str("\tif cfg.RedactFn != nil {\n");
    out.push_str("\t\tSetRedactFn(cfg.RedactFn)\n");
    out.push_str("\t}\n");
    out.push_str("}\n");
}

fn write_dispatch_fn(out: &mut String) {
    out.push('\n');
    out.push_str("// _dispatch fans out a single event to all configured providers.\n");
    out.push_str("func _dispatch(name string, props map[string]any) {\n");
    out.push_str("\t_mu.RLock()\n");
    out.push_str("\tconsent := _consentState\n");
    out.push_str("\tredact := _redactFn\n");
    out.push_str("\tproviders := _providers\n");
    out.push_str("\t_mu.RUnlock()\n");
    out.push('\n');
    out.push_str("\tif consent == ConsentDenied {\n");
    out.push_str("\t\treturn\n");
    out.push_str("\t}\n");
    out.push_str("\tif redact != nil {\n");
    out.push_str("\t\tfor k, v := range props {\n");
    out.push_str("\t\t\tprops[k] = redact(name, k, v)\n");
    out.push_str("\t\t}\n");
    out.push_str("\t}\n");
    out.push_str("\tfor _, p := range providers {\n");
    out.push_str("\t\tp.Track(name, props)\n");
    out.push_str("\t}\n");
    out.push_str("}\n");
}

fn write_event_name_constants(out: &mut String, events: &[&CatalogEntry]) {
    out.push('\n');
    if events.is_empty() {
        out.push_str("// No events are currently approved in the catalog.\n");
        return;
    }
    out.push_str("// EventName constants enumerate all tracked event names.\n");
    out.push_str("const (\n");
    for entry in events {
        let pascal = to_go_pascal_case(&entry.name);
        out.push_str(&format!(
            "\tEventName{pascal} = \"{}\"\n",
            entry.name
        ));
    }
    out.push_str(")\n");
}

fn write_property_struct(out: &mut String, entry: &CatalogEntry) {
    let pascal = to_go_pascal_case(&entry.name);
    let mut sorted_props: Vec<&EventProperty> = entry.properties.iter().collect();
    sorted_props.sort_by(|a, b| a.name.cmp(&b.name));

    if !entry.description.is_empty() {
        out.push_str(&format!(
            "// {pascal}Properties holds properties for the {} event.\n",
            entry.name
        ));
        out.push_str(&format!("// {}\n", entry.description));
    } else {
        out.push_str(&format!(
            "// {pascal}Properties holds properties for the {} event.\n",
            entry.name
        ));
    }

    if sorted_props.is_empty() {
        // Use a type alias to map[string]any for prop-less events (mirrors TS Record<string,never>).
        out.push_str(&format!(
            "type {pascal}Properties = map[string]any\n"
        ));
    } else {
        out.push_str(&format!("type {pascal}Properties struct {{\n"));
        for prop in &sorted_props {
            let field_name = to_go_pascal_case(&prop.name);
            let go_t = go_type(prop.prop_type.as_deref());
            if prop.pii {
                out.push_str(&format!(
                    "\t{field_name} {go_t} `json:\"{}\"` // pii:\"true\"\n",
                    prop.name
                ));
            } else {
                out.push_str(&format!(
                    "\t{field_name} {go_t} `json:\"{}\"`\n",
                    prop.name
                ));
            }
        }
        out.push_str("}\n");
    }
}

fn write_track_function(out: &mut String, entry: &CatalogEntry) {
    let pascal = to_go_pascal_case(&entry.name);
    out.push('\n');
    if entry.description.is_empty() {
        out.push_str(&format!("// Track{pascal} sends the {} event.\n", entry.name));
    } else {
        out.push_str(&format!(
            "// Track{pascal} sends the {} event.\n//\n// {}\n",
            entry.name, entry.description
        ));
    }
    out.push_str(&format!(
        "func Track{pascal}(props {pascal}Properties) {{\n"
    ));
    out.push_str("\tb, _ := json.Marshal(props)\n");
    out.push_str("\tvar m map[string]any\n");
    out.push_str("\t_ = json.Unmarshal(b, &m)\n");
    out.push_str(&format!("\t_dispatch(EventName{pascal}, m)\n"));
    out.push_str("}\n");
}

fn write_event_section(out: &mut String, entry: &CatalogEntry) {
    let pascal = to_go_pascal_case(&entry.name);
    out.push('\n');
    let prefix = format!("// ─── {} ", entry.name);
    let bar_len = 80usize.saturating_sub(prefix.len());
    let bar = "─".repeat(bar_len.max(2));
    out.push_str(&format!("{}{}\n", prefix, bar));
    out.push('\n');
    write_property_struct(out, entry);
    write_track_function(out, entry);
    let _ = pascal;
}

fn write_track_namespace(out: &mut String, events: &[&CatalogEntry]) {
    out.push('\n');
    if events.is_empty() {
        out.push_str("// Track is the namespace for all tracked events.\n");
        out.push_str("// No events are currently approved in the catalog.\n");
        out.push_str("var Track = struct{}{}\n");
        return;
    }

    out.push_str("// Track is the namespace for all tracked events.\n");
    out.push_str("//\n");
    out.push_str("// Example:\n");
    let first_pascal = to_go_pascal_case(&events[0].name);
    out.push_str(&format!("//   Track.{first_pascal}({first_pascal}Properties{{}})\n"));
    out.push_str("var Track = struct {\n");
    for entry in events {
        let pascal = to_go_pascal_case(&entry.name);
        out.push_str(&format!("\t{pascal} func({pascal}Properties)\n"));
    }
    out.push_str("}{\n");
    for entry in events {
        let pascal = to_go_pascal_case(&entry.name);
        out.push_str(&format!("\t{pascal}: Track{pascal},\n"));
    }
    out.push_str("}\n");
}

