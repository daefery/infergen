//! Go code generation from an approved event catalog (E5.2).
//!
//! `generate_go` produces a deterministic, idiomatic Go package: typed property
//! structs, track functions, event-name constants, a `Track` namespace var,
//! a `Provider` interface, and a runtime with consent, region, and redaction
//! hooks.  Output is deterministic (events + properties sorted alphabetically,
//! no timestamps) and safe to commit or `.gitignore`.

use infergen_types::{CatalogEntry, EventProperty, EventStatus};

use crate::{Catalog, CATALOG_SCHEMA_VERSION};

// ---------------------------------------------------------------------------
// Public API
// ---------------------------------------------------------------------------

/// Configuration for Go code generation.
#[derive(Debug, Clone)]
pub struct GoCodegenConfig {
    /// Also emit `Proposed` events in addition to `Approved`.
    ///
    /// Default: `false`.
    pub include_proposed: bool,
    /// Go package name for the generated file.
    ///
    /// Default: `"infergen"`.
    pub package_name: String,
}

impl Default for GoCodegenConfig {
    fn default() -> Self {
        Self {
            include_proposed: false,
            package_name: "infergen".to_owned(),
        }
    }
}

/// Generate a Go package source string from an approved catalog.
///
/// Only `Approved` entries are included by default; `Ignored` entries are
/// always excluded.  Set `config.include_proposed = true` to also include
/// `Proposed` entries.
///
/// Output is deterministic: events sorted alphabetically, properties sorted
/// alphabetically within each struct.
///
/// # Examples
/// ```rust
/// use infergen_core::codegen::{GoCodegenConfig, generate_go};
/// use infergen_core::Catalog;
/// let go = generate_go(&Catalog::default(), &GoCodegenConfig::default());
/// assert!(go.contains("package infergen"));
/// ```
#[must_use]
pub fn generate_go(catalog: &Catalog, config: &GoCodegenConfig) -> String {
    let mut events: Vec<&CatalogEntry> = catalog
        .events
        .iter()
        .filter(|e| {
            e.status == EventStatus::Approved
                || (config.include_proposed && e.status == EventStatus::Proposed)
        })
        .collect();
    events.sort_by(|a, b| a.name.cmp(&b.name));

    let mut out = String::new();
    write_header(&mut out, config);
    write_imports(&mut out);
    write_provider_interface(&mut out);
    write_runtime_state(&mut out);
    write_consent_module(&mut out);
    write_configure_fn(&mut out);
    write_dispatch_fn(&mut out);
    write_event_name_constants(&mut out, &events);

    for entry in &events {
        write_event_section(&mut out, entry);
    }

    write_track_namespace(&mut out, &events);
    out
}

// ---------------------------------------------------------------------------
// Private helpers — name conversion
// ---------------------------------------------------------------------------

/// Convert any snake_case / camelCase identifier to PascalCase.
fn to_go_pascal_case(name: &str) -> String {
    crate::namer::split_identifier(name)
        .into_iter()
        .map(|w| {
            let mut chars = w.chars();
            match chars.next() {
                None => String::new(),
                Some(f) => f.to_uppercase().to_string() + chars.as_str(),
            }
        })
        .collect()
}

/// Map a catalog `prop_type` to a Go type string.
fn go_type(prop_type: Option<&str>) -> &'static str {
    match prop_type {
        Some("string") => "string",
        Some("number") => "float64",
        Some("boolean") => "bool",
        _ => "any",
    }
}

