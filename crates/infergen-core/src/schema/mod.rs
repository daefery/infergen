#[cfg(test)]
mod tests {
    use super::*;
    use infergen_types::{
        CatalogEntry, CatalogEventKind, EventProvenance, EventProperty, EventStatus,
        CATALOG_SCHEMA_VERSION,
    };

    fn make_catalog(entries: Vec<CatalogEntry>) -> Catalog {
        Catalog { schema_version: CATALOG_SCHEMA_VERSION, events: entries }
    }

    fn make_entry(name: &str, status: EventStatus) -> CatalogEntry {
        CatalogEntry {
            id: format!("evt_{name}"),
            name: name.to_owned(),
            description: String::new(),
            status,
            confidence: 0.9,
            kind: CatalogEventKind::PageView,
            provenance: vec![EventProvenance {
                source_path: "src/index.tsx".into(),
                line: None,
                adapter: "nextjs".into(),
            }],
            properties: Vec::new(),
            providers: Vec::new(),
        }
    }

    fn make_prop(name: &str, t: Option<&str>, pii: bool) -> EventProperty {
        EventProperty { name: name.into(), prop_type: t.map(Into::into), required: false, pii }
    }

    #[test]
    fn schema_postgres_has_create_table_infergen_events() {
        let sql = generate_sql_schema(&Catalog::default(), SqlDialect::Postgres);
        assert!(
            sql.contains(r#"CREATE TABLE IF NOT EXISTS "infergen_events""#),
            "output:\n{sql}"
        );
    }

    #[test]
    fn schema_mysql_has_create_table_backtick_quoted() {
        let sql = generate_sql_schema(&Catalog::default(), SqlDialect::Mysql);
        assert!(
            sql.contains("CREATE TABLE IF NOT EXISTS `infergen_events`"),
            "output:\n{sql}"
        );
    }

    #[test]
    fn schema_sqlite_has_create_table_double_quoted() {
        let sql = generate_sql_schema(&Catalog::default(), SqlDialect::Sqlite);
        assert!(
            sql.contains(r#"CREATE TABLE IF NOT EXISTS "infergen_events""#),
            "output:\n{sql}"
        );
        assert!(sql.contains("AUTOINCREMENT"), "output:\n{sql}");
    }

    #[test]
    fn schema_postgres_has_jsonb_column() {
        let sql = generate_sql_schema(&Catalog::default(), SqlDialect::Postgres);
        assert!(sql.contains("JSONB"), "output:\n{sql}");
    }

    #[test]
    fn schema_mysql_has_json_column() {
        let sql = generate_sql_schema(&Catalog::default(), SqlDialect::Mysql);
        assert!(sql.contains("JSON"), "output:\n{sql}");
        assert!(!sql.contains("JSONB"), "MySQL should not use JSONB\noutput:\n{sql}");
    }

    #[test]
    fn schema_sqlite_has_text_column_for_properties() {
        let sql = generate_sql_schema(&Catalog::default(), SqlDialect::Sqlite);
        assert!(sql.contains("properties TEXT"), "output:\n{sql}");
        assert!(!sql.contains("JSONB"), "SQLite should not use JSONB\noutput:\n{sql}");
    }

    #[test]
    fn schema_empty_catalog_has_no_views() {
        let sql = generate_sql_schema(&Catalog::default(), SqlDialect::Postgres);
        assert!(!sql.contains("CREATE VIEW"), "unexpected view\noutput:\n{sql}");
        assert!(!sql.contains("CREATE OR REPLACE VIEW"), "unexpected view\noutput:\n{sql}");
    }

    #[test]
    fn schema_single_event_no_props_no_view() {
        let cat = make_catalog(vec![make_entry("page_viewed", EventStatus::Approved)]);
        let sql = generate_sql_schema(&cat, SqlDialect::Postgres);
        assert!(!sql.contains("CREATE OR REPLACE VIEW"), "no-props event should not have view\noutput:\n{sql}");
    }

    #[test]
    fn schema_single_event_with_props_has_view_postgres() {
        let mut entry = make_entry("page_viewed", EventStatus::Approved);
        entry.properties.push(make_prop("route", Some("string"), false));
        let cat = make_catalog(vec![entry]);
        let sql = generate_sql_schema(&cat, SqlDialect::Postgres);
        assert!(
            sql.contains(r#"CREATE OR REPLACE VIEW "page_viewed""#),
            "view missing\noutput:\n{sql}"
        );
        assert!(sql.contains("properties ->> 'route'"), "json extract missing\noutput:\n{sql}");
    }

    #[test]
    fn schema_event_view_includes_pii_comment() {
        let mut entry = make_entry("user_signed_in", EventStatus::Approved);
        entry.properties.push(make_prop("email", Some("string"), true));
        let cat = make_catalog(vec![entry]);
        let sql = generate_sql_schema(&cat, SqlDialect::Postgres);
        assert!(sql.contains("-- @pii"), "PII comment missing\noutput:\n{sql}");
    }

    #[test]
    fn schema_mysql_view_uses_json_unquote_extract() {
        let mut entry = make_entry("page_viewed", EventStatus::Approved);
        entry.properties.push(make_prop("route", Some("string"), false));
        let cat = make_catalog(vec![entry]);
        let sql = generate_sql_schema(&cat, SqlDialect::Mysql);
        assert!(sql.contains("JSON_UNQUOTE(JSON_EXTRACT"), "MySQL json extract missing\noutput:\n{sql}");
    }

    #[test]
    fn schema_sqlite_view_uses_json_extract_fn() {
        let mut entry = make_entry("page_viewed", EventStatus::Approved);
        entry.properties.push(make_prop("route", Some("string"), false));
        let cat = make_catalog(vec![entry]);
        let sql = generate_sql_schema(&cat, SqlDialect::Sqlite);
        assert!(
            sql.contains("CREATE VIEW IF NOT EXISTS"),
            "SQLite view syntax missing\noutput:\n{sql}"
        );
        assert!(
            sql.contains("json_extract(properties, '$.route')"),
            "SQLite json_extract missing\noutput:\n{sql}"
        );
    }

    #[test]
    fn schema_events_alphabetically_sorted_in_views() {
        let mut z = make_entry("zebra_event", EventStatus::Approved);
        z.properties.push(make_prop("x", None, false));
        let mut a = make_entry("alpha_event", EventStatus::Approved);
        a.properties.push(make_prop("x", None, false));
        let cat = make_catalog(vec![z, a]);
        let sql = generate_sql_schema(&cat, SqlDialect::Postgres);
        let alpha_pos = sql.find("alpha_event").unwrap();
        let zebra_pos = sql.find("zebra_event").unwrap();
        assert!(alpha_pos < zebra_pos, "views not in alphabetical order\noutput:\n{sql}");
    }

    #[test]
    fn schema_ignored_event_no_view() {
        let mut entry = make_entry("noise_event", EventStatus::Ignored);
        entry.properties.push(make_prop("route", None, false));
        let cat = make_catalog(vec![entry]);
        let sql = generate_sql_schema(&cat, SqlDialect::Postgres);
        assert!(!sql.contains("noise_event"), "ignored event should not appear\noutput:\n{sql}");
    }

    #[test]
    fn schema_proposed_event_no_view() {
        let mut entry = make_entry("maybe_event", EventStatus::Proposed);
        entry.properties.push(make_prop("route", None, false));
        let cat = make_catalog(vec![entry]);
        let sql = generate_sql_schema(&cat, SqlDialect::Postgres);
        assert!(!sql.contains("maybe_event"), "proposed event should not appear\noutput:\n{sql}");
    }

    #[test]
    fn schema_header_contains_dialect_name() {
        for (dialect, marker) in [
            (SqlDialect::Postgres, "postgres"),
            (SqlDialect::Mysql, "mysql"),
            (SqlDialect::Sqlite, "sqlite"),
        ] {
            let sql = generate_sql_schema(&Catalog::default(), dialect);
            assert!(sql.contains(marker), "dialect marker '{marker}' missing\noutput:\n{sql}");
        }
    }

    #[test]
    fn schema_deterministic() {
        let mut entry = make_entry("page_viewed", EventStatus::Approved);
        entry.properties.push(make_prop("route", Some("string"), false));
        let cat = make_catalog(vec![entry]);
        let sql1 = generate_sql_schema(&cat, SqlDialect::Postgres);
        let sql2 = generate_sql_schema(&cat, SqlDialect::Postgres);
        assert_eq!(sql1, sql2);
    }
}
