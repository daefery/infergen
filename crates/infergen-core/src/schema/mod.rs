//! SQL schema generation from an approved event catalog (E3.2b).
//!
//! `generate_sql_schema` produces a deterministic SQL file with a `CREATE TABLE`
//! for the `infergen_events` table and a `CREATE VIEW` per approved event that
//! has at least one property, extracting each property from the JSON column.

use infergen_types::{CatalogEntry, EventProperty, EventStatus};

use crate::{Catalog, CATALOG_SCHEMA_VERSION};

// ---------------------------------------------------------------------------
// Public API
// ---------------------------------------------------------------------------

/// SQL dialect for schema generation.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum SqlDialect {
    /// PostgreSQL — uses `JSONB`, `BIGSERIAL`, `TIMESTAMPTZ`.
    Postgres,
    /// MySQL / MariaDB — uses `JSON`, `BIGINT UNSIGNED AUTO_INCREMENT`, `DATETIME`.
    Mysql,
    /// SQLite — uses `TEXT` for properties, `INTEGER AUTOINCREMENT`.
    Sqlite,
}

impl SqlDialect {
    /// Lowercase string form of the dialect name.
    #[must_use]
    pub fn as_str(self) -> &'static str {
        match self {
            SqlDialect::Postgres => "postgres",
            SqlDialect::Mysql => "mysql",
            SqlDialect::Sqlite => "sqlite",
        }
    }
}

/// Generate a SQL schema string from an approved catalog.
///
/// Output includes:
/// 1. A `CREATE TABLE IF NOT EXISTS infergen_events` table.
/// 2. An index on `event_name`.
/// 3. A typed `CREATE VIEW` per approved event that has at least one property.
///
/// Only `Approved` entries are included. Output is deterministic.
#[must_use]
pub fn generate_sql_schema(catalog: &Catalog, dialect: SqlDialect) -> String {
    let mut events: Vec<&CatalogEntry> = catalog
        .events
        .iter()
        .filter(|e| e.status == EventStatus::Approved)
        .collect();
    events.sort_by(|a, b| a.name.cmp(&b.name));

    let mut out = String::new();
    write_schema_header(&mut out, dialect);
    write_events_table(&mut out, dialect);
    write_events_index(&mut out, dialect);

    let events_with_props: Vec<&&CatalogEntry> =
        events.iter().filter(|e| !e.properties.is_empty()).collect();

    if !events_with_props.is_empty() {
        out.push('\n');
        for entry in events_with_props {
            write_event_view(&mut out, entry, dialect);
        }
    }

    out
}

// ---------------------------------------------------------------------------
// Private helpers
// ---------------------------------------------------------------------------

fn quote_identifier(name: &str, dialect: SqlDialect) -> String {
    match dialect {
        SqlDialect::Postgres | SqlDialect::Sqlite => format!("\"{name}\""),
        SqlDialect::Mysql => format!("`{name}`"),
    }
}

fn json_extract_expr(field: &str, dialect: SqlDialect) -> String {
    match dialect {
        SqlDialect::Postgres => format!("properties ->> '{field}'"),
        SqlDialect::Mysql => {
            format!("JSON_UNQUOTE(JSON_EXTRACT(properties, '$.{field}'))")
        }
        SqlDialect::Sqlite => format!("json_extract(properties, '$.{field}')"),
    }
}

fn write_schema_header(out: &mut String, dialect: SqlDialect) {
    out.push_str("-- Auto-generated by Infergen. Do not edit — run `infergen schema` to regenerate.\n");
    out.push_str(&format!(
        "-- infergen-core v{} · catalog schema v{} · dialect: {}\n",
        env!("CARGO_PKG_VERSION"),
        CATALOG_SCHEMA_VERSION,
        dialect.as_str(),
    ));
}

fn write_events_table(out: &mut String, dialect: SqlDialect) {
    out.push('\n');
    match dialect {
        SqlDialect::Postgres => {
            out.push_str("CREATE TABLE IF NOT EXISTS \"infergen_events\" (\n");
            out.push_str("  id BIGSERIAL PRIMARY KEY,\n");
            out.push_str("  event_name TEXT NOT NULL,\n");
            out.push_str("  properties JSONB NOT NULL DEFAULT '{}',\n");
            out.push_str("  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()\n");
            out.push_str(");\n");
        }
        SqlDialect::Mysql => {
            out.push_str("CREATE TABLE IF NOT EXISTS `infergen_events` (\n");
            out.push_str("  id BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY,\n");
            out.push_str("  event_name VARCHAR(255) NOT NULL,\n");
            out.push_str("  properties JSON NOT NULL,\n");
            out.push_str("  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,\n");
            out.push_str("  INDEX infergen_events_event_name_idx (event_name)\n");
            out.push_str(");\n");
        }
        SqlDialect::Sqlite => {
            out.push_str("CREATE TABLE IF NOT EXISTS \"infergen_events\" (\n");
            out.push_str("  id INTEGER PRIMARY KEY AUTOINCREMENT,\n");
            out.push_str("  event_name TEXT NOT NULL,\n");
            out.push_str("  properties TEXT NOT NULL DEFAULT '{}',\n");
            out.push_str("  created_at TEXT NOT NULL DEFAULT (datetime('now'))\n");
            out.push_str(");\n");
        }
    }
}

fn write_events_index(out: &mut String, dialect: SqlDialect) {
    match dialect {
        // MySQL inlines the index inside CREATE TABLE.
        SqlDialect::Mysql => {}
        SqlDialect::Postgres => {
            out.push('\n');
            out.push_str(
                "CREATE INDEX IF NOT EXISTS infergen_events_event_name_idx\n  ON \"infergen_events\" (event_name);\n",
            );
        }
        SqlDialect::Sqlite => {
            out.push('\n');
            out.push_str(
                "CREATE INDEX IF NOT EXISTS infergen_events_event_name_idx\n  ON \"infergen_events\" (event_name);\n",
            );
        }
    }
}

fn write_event_view(out: &mut String, entry: &CatalogEntry, dialect: SqlDialect) {
    let tbl = quote_identifier("infergen_events", dialect);
    let view = quote_identifier(&entry.name, dialect);

    let mut sorted_props: Vec<&EventProperty> = entry.properties.iter().collect();
    sorted_props.sort_by(|a, b| a.name.cmp(&b.name));

    out.push_str(&format!("-- {}\n", entry.name));

    match dialect {
        SqlDialect::Postgres | SqlDialect::Mysql => {
            out.push_str(&format!("CREATE OR REPLACE VIEW {view} AS\n"));
        }
        SqlDialect::Sqlite => {
            out.push_str(&format!("CREATE VIEW IF NOT EXISTS {view} AS\n"));
        }
    }

    out.push_str("  SELECT\n    id,\n    created_at");

    for prop in &sorted_props {
        let extract = json_extract_expr(&prop.name, dialect);
        let pii_suffix = if prop.pii { " -- @pii" } else { "" };
        out.push_str(&format!(",\n    {extract} AS {}{pii_suffix}", prop.name));
    }

    out.push_str(&format!(
        "\n  FROM {tbl}\n  WHERE event_name = '{}';\n\n",
        entry.name
    ));
}

// ---------------------------------------------------------------------------
// Unit tests
